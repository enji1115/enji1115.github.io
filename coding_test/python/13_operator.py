# 산술
print(3 + 3) # 6
print(20 - 5) # 15
print(6 * 2) # 12
print(12 / 3) # 4.0
# 특수 연산자
print(3 ** 2)  # 3의 2제곱 # 9
print(4 ** 3)  # 4의 3제곱 # 64
print(7 / 3)  # 7 나누기 3 # 2.3333333333333335
print(7 // 3)  # 7 나누기 3의 몫 # 2
print(7 % 3)  # 7 나누기 3의 나머지 # 1


# 비교


# 논리: and or not


# 멤버쉽: in, not in : 리스트, 튜플 등의 안에 해당하는 값이 있는 지 확인
fruits = ['사과', '딸기', '망고', '브로콜리', '바나나']
print(fruits) # ['사과', '딸기', '망고', '브로콜리', '바나나']
print('딸기' in fruits) # True
print('딸기' not in fruits) # False
print('상추' in fruits) # False
print('상추' not in fruits) # True
